#include "CLI_time.h"

#ifdef __linux__
#  include <sys/time.h>
#  include <unistd.h>
#endif

void PIL_sleep_ms(int ms)
{
  if (ms >= 1000) {
    sleep(ms / 1000);
    ms = (ms % 1000);
  }

  usleep(ms * 1000);
}
